function sbteGoTo(targetIdx) {
  if (sbteAnimating) return;
  sbteAnimating = true;

  const { direction, steps } = sbteGetDirection(targetIdx);

  // Left panel exit → enter
  sbtePlayExit(() => {
    sbtePlayEnter(SBTE_PRODUCTS[targetIdx]);
  });

  // Right panel scroll
  if (direction === "up") {
    sbteScrollUpBy(steps, () => {});
  } else {
    sbteScrollDownBy(steps, () => {});
  }
}

