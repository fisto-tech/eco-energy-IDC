// ─────────────────────────────────────────────────────────────
// 7. SCROLL UP — belt moves up (next product becomes active)
//    Exiting item (was slot-0) teleports silently to bottom
//    and becomes the new slot-4 on the next scroll — true loop.
// ─────────────────────────────────────────────────────────────
function sbScrollUpBy(steps, onComplete) {
  const newCenter  = (sbCenter + steps + SB_TOTAL) % SB_TOTAL;
  const oldVisible = sbGetVisible(sbCenter);
  const newVisible = sbGetVisible(newCenter);
  const oldSet     = new Set(oldVisible);
  const newSet     = new Set(newVisible);

  // With 5 products total = 5 visible, steps=1 means:
  //   exiting = 1 item (was slot-0, leaves upward)
  //   entering = 1 item (now slot-4, comes from bottom)
  //   staying  = 4 items (slide up one slot)
  const exiting  = oldVisible.filter(i => !newSet.has(i));
  const entering = newVisible.filter(i => !oldSet.has(i));
  const staying  = newVisible.filter(i =>  oldSet.has(i));

  // 1. Exit upward
  exiting.forEach(idx => {
    const el = document.getElementById(`SB-16-side-${idx}`);
    if (!el) return;
    sbClearSlotClasses(el);
    el.classList.add("sb-slot-exit-up");
  });

  // 2. Staying → slide to new slot position
  staying.forEach(idx => {
    const el = document.getElementById(`SB-16-side-${idx}`);
    if (!el) return;
    sbClearSlotClasses(el);
    el.classList.add(`sb-slot-${newVisible.indexOf(idx)}`);
  });

  // 3. Entering → instantly place below viewport (no transition)
  entering.forEach(idx => {
    const el = document.getElementById(`SB-16-side-${idx}`);
    if (!el) return;
    sbClearSlotClasses(el);
    el.style.transition = 'none';
    el.classList.add("sb-slot-enter-bottom");
  });

  // 4. After micro-delay → slide entering item up into slot
  setTimeout(() => {
    entering.forEach(idx => {
      const el = document.getElementById(`SB-16-side-${idx}`);
      if (!el) return;
      el.offsetHeight; // force reflow
      el.style.transition = '';
      sbClearSlotClasses(el);
      el.classList.add(`sb-slot-${newVisible.indexOf(idx)}`);
    });

    // 5. After animation settles → silently park exiting item below (ready to loop)
    setTimeout(() => {
      exiting.forEach(idx => {
        const el = document.getElementById(`SB-16-side-${idx}`);
        if (!el) return;
        el.style.transition = 'none';
        sbClearSlotClasses(el);
        el.classList.add("sb-slot-enter-bottom");
        el.offsetHeight;
        el.style.transition = '';
      });
      sbCenter = newCenter;
      if (onComplete) onComplete();
    }, 550);
  }, 30);
}

// ─────────────────────────────────────────────────────────────
// 8. SCROLL DOWN — belt moves down (previous product activates)
// ─────────────────────────────────────────────────────────────
function sbScrollDownBy(steps, onComplete) {
  const newCenter  = (sbCenter - steps + SB_TOTAL) % SB_TOTAL;
  const oldVisible = sbGetVisible(sbCenter);
  const newVisible = sbGetVisible(newCenter);
  const oldSet     = new Set(oldVisible);
  const newSet     = new Set(newVisible);

  const exiting  = oldVisible.filter(i => !newSet.has(i));
  const entering = newVisible.filter(i => !oldSet.has(i));
  const staying  = newVisible.filter(i =>  oldSet.has(i));

  // 1. Exit downward
  exiting.forEach(idx => {
    const el = document.getElementById(`SB-16-side-${idx}`);
    if (!el) return;
    sbClearSlotClasses(el);
    el.classList.add("sb-slot-exit-down");
  });

  // 2. Staying → slide to new slot
  staying.forEach(idx => {
    const el = document.getElementById(`SB-16-side-${idx}`);
    if (!el) return;
    sbClearSlotClasses(el);
    el.classList.add(`sb-slot-${newVisible.indexOf(idx)}`);
  });

  // 3. Entering → instantly place above viewport
  entering.forEach(idx => {
    const el = document.getElementById(`SB-16-side-${idx}`);
    if (!el) return;
    sbClearSlotClasses(el);
    el.style.transition = 'none';
    el.classList.add("sb-slot-enter-top");
  });

  // 4. Micro-delay → slide entering item down into slot
  setTimeout(() => {
    entering.forEach(idx => {
      const el = document.getElementById(`SB-16-side-${idx}`);
      if (!el) return;
      el.offsetHeight;
      el.style.transition = '';
      sbClearSlotClasses(el);
      el.classList.add(`sb-slot-${newVisible.indexOf(idx)}`);
    });

    // 5. Park exiting item above (ready to loop back in)
    setTimeout(() => {
      exiting.forEach(idx => {
        const el = document.getElementById(`SB-16-side-${idx}`);
        if (!el) return;
        el.style.transition = 'none';
        sbClearSlotClasses(el);
        el.classList.add("sb-slot-enter-top");
        el.offsetHeight;
        el.style.transition = '';
      });
      sbCenter = newCenter;
      if (onComplete) onComplete();
    }, 550);
  }, 30);
}

