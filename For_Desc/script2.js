// 11. GOTO
// ─────────────────────────────────────────────────────────────
function sbGoTo(targetIdx) {
  if (sbAnimating) return;
  sbAnimating = true;

  const { direction, steps } = sbGetDirection(targetIdx);

  sbPlayExit(() => sbPlayEnter(SB_PRODUCTS[targetIdx]));

  if (direction === "up") {
    sbScrollUpBy(steps, () => {});
  } else {
    sbScrollDownBy(steps, () => {});
  }
}

